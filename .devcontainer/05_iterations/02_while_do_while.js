// while

let index = 1
while (index < 20) {
    // console.log(index);
    index += 5
}

let myArray = ["flash", "batman", "superman"]
let arr = 0 
while (arr < myArray.length) {
    console.log(`value is ${myArray[arr]}`);
    arr += 1
}

// do_while

let score = 100

do {
    console.log(`score is ${score}`);
    score += 100
    
} while (score <= 1000);